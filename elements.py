def sum_list_elements(numbers):
    total = 0
    for num in numbers:
        total += num
    return total

# Example
print(sum_list_elements([1, 2, 3, 4, 5]))  # Output: 15
#explanation
#You loop through each item in the list.

# Then you add each item to a total variable.
#The return is the final total.
